// A stable message for a given calendar day, so the whole community shares the
// same one — a small daily ritual. Deterministic: same day → same message.
export function messageOfTheDay(now = new Date()): DailyMessage {
  const dayNumber = Math.floor(
    Date.UTC(now.getFullYear(), now.getMonth(), now.getDate()) / 86400000,
  );
  const n = DAILY_MESSAGES.length;
  return DAILY_MESSAGES[((dayNumber % n) + n) % n];
}
